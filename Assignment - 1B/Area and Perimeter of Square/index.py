# You are given a side of square as input. Write a program to find the perimeter and area of the square.

number = int(input()) # Input: 3

area_of_square = number * number
perimeter_of_square = 4 * number

print("Area of the square is: " + str(area_of_square)) # Area of the square is: 9
print("Perimeter of the square is: " + str(perimeter_of_square)) # Perimeter of the square is: 12